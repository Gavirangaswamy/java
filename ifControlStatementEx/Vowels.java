class Vowels{

public static void main(String[] args) {
       
    	char ch = 'A' ;
    	
    
        if (ch=='A'){
            System.out.println("Character A is a vowel");
        }else if(ch== 'E'){
            System.out.println("Character E is a vowel");
        }else if(ch== 'I'){
            System.out.println("Character I is a vowel");
        }else if(ch== 'O'){
            System.out.println("Character O is a vowel");
        }else if(ch== 'U'){
            System.out.println("Character U is a vowel");
        }else{
        	System.out.println("Character is not a vowel");
        }
      
         
    }
}